#include <stdio.h>

int main(void)
{
	int temperature;

    // Ask user to enter temperature
    
    printf("Enter temperature: ");
    scanf("%d", &temperature);

    // If temperature is greater than 30, print Hot
    if (temperature > 30) {
        printf("Weather is Hot\n");
    }

    return 0;
}
	
	
	
	
	
	
	
	
	
	

